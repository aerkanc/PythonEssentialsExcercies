# İşleç yada teknik ismiyle operatör
# değişkenlerin içerdiği veriyi işler.

# Atama İşleci =
sayi1 = 16
sayi2 = 7

# Aritmetik Operatörler:
# Toplama +
toplam = sayi1 + sayi2

# Ekleme +=
sayi1 += 5
# sayi1 artık 16+5 => 21 oldu
# aslında şu da aynıdır
# sayi1 = sayi1 + 5

# Çıkarma -
fark = sayi1 - sayi2

# Eksiltme -=
sayi1 -= 5
# en son sayi1 21 olmuştu.
# şimdi ise 21 - 5 => 16 oldu
# aslında şu da aynıdır
# sayi1 = sayi1 - 5

# Bölme /
bolum = sayi1 / sayi2

# Bölümünü alma /=
sayi1 /= 2
# sayi1 artık 16 / 2 => 8 oldu
# aslında şu da aynıdır
# sayi1 = sayi1 / 2

# Çarpma *
carpim = sayi1 * sayi2

# Katını alma *=
sayi1 *= 2
# sayi1 en son sayi1 8 olmuştu.
# sayi1 artık 16 * 2 => 16 oldu
# aslında şu da aynıdır
# sayi1 = sayi1 * 2


# Tam Bölme
tam_bolum = sayi1 // sayi2

# mod %
kalan = sayi1 % sayi2

# kuvvet işleci **
kare = sayi1 ** 2


